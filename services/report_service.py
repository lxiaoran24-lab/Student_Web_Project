def build_overall_summary(overall_score, academic_score, consumption_score, health_score, psychology_score):
    scores = {
        "学业": academic_score,
        "消费": consumption_score,
        "身体": health_score,
        "心理": psychology_score,
    }
    strongest = max(scores, key=scores.get)
    weakest = min(scores, key=scores.get)

    overall_level = score_to_level_name(overall_score)

    return (
        f"本次评估基于学业、消费、健康、心理四大维度，综合得分为 {overall_score} 分，"
        f"整体处于{overall_level}水平。当前核心优势维度为{strongest}，核心短板维度为{weakest}。"
        f"整体来看，你具备进一步优化和提升的空间，建议继续巩固优势，同时优先补足短板。"
    )


