    def get_similarity(self, code_a, code_b):
        """
        计算两个专业代码的相似度
        """
        # 1. 基础校验
        code_a = str(code_a).strip()
        code_b = str(code_b).strip()

        if len(code_a) < 4 or len(code_b) < 4:
            return "错误：专业代码至少需要4位 (如 0809)"

        # 2. 提取层级
        # 门类代码 (前2位)
        cat_a = code_a[:2]
        cat_b = code_b[:2]

        # 专业类代码 (前4位)
        class_a = code_a[:4]
        class_b = code_b[:4]

        # 检查代码有效性
        if cat_a not in self.matrix or cat_b not in self.matrix:
            return f"错误：无法识别的门类代码 '{cat_a}' 或 '{cat_b}'"

        # 3. 计算逻辑
        score = 0.0
        details = []

        # C1: 门类一致性
        score_c1 = 0
        if cat_a == cat_b:
            score_c1 = 100 * self.w1_category
            details.append(f"门类相同 (+{score_c1:.2f})")
        else:
            details.append("门类不同 (+0)")

        # C2: 专业类一致性
        score_c2 = 0
        if class_a == class_b:
            score_c2 = 100 * self.w2_class
            details.append(f"专业类相同 (+{score_c2:.2f})")
        else:
            details.append("专业类不同 (+0)")

        # C3: 门类矩阵修正
        # 查找矩阵中的相关系数 R(A,B)
        r_val = self.matrix[cat_a].get(cat_b, 0.0)

        # 文档逻辑：Total = ... + 100 * R * W3
        score_c3 = 100 * r_val * self.w3_matrix
        details.append(f"矩阵修正 (R={r_val}) -> (+{score_c3:.2f})")

        # 4. 总分汇总
        total_score = score_c1 + score_c2 + score_c3

        # 如果完全相同，强制为100 (避免浮点误差)
        if code_a == code_b:
            total_score = 100.0

        return {
            "score": round(total_score, 2),
            "details": details,
            "names": (self.cat_names.get(cat_a), self.cat_names.get(cat_b))
        }


